 /*
App.js
    Состояние:
        events: 
            Массив мероприятий.
        showForm: 
            Булево значение, указывающее, отображается ли форма добавления мероприятия.
        selectedEvent: 
            Выбранное мероприятие для отображения деталей.
        editingEvent: 
            Мероприятие, которое редактируется.
        showTeams: 
            ID мероприятия, для которого отображаются команды.
    Функции:
        addEvent: 
            Добавляет новое мероприятие в массив events и скрывает форму.
        updateEvent: 
            Обновляет мероприятие в массиве events и скрывает форму редактирования.
        deleteEvent: 
            Удаляет мероприятие из массива events и скрывает детали мероприятия.
        viewEventDetails: 
            Устанавливает выбранное мероприятие для отображения деталей.
        editEvent: 
            Устанавливает мероприятие для редактирования и отображает его детали.
        closeDetails: 
            Скрывает детали мероприятия и форму редактирования.
        toggleTeams: 
            Переключает отображение команд для мероприятия.
    Рендеринг:
            Отображается заголовок "Мероприятия" в прямоугольнике с черной обводкой.
            Кнопка "+" для добавления нового мероприятия отображается в виде картинки плюса.
            Контейнер для списка мероприятий и формы добавления.
            Если showForm истинно, отображается EventForm.
            EventList теперь принимает дополнительные пропсы для управления деталями, редактированием мероприятий и отображением команд.

       

EventList.js
        Пропсы:
            events: Массив мероприятий.
            onViewDetails: 
                Функция для отображения деталей мероприятия.
            selectedEvent: 
                Выбранное мероприятие для отображения деталей.
            onEditEvent: 
                Функция для редактирования мероприятия.
            onDeleteEvent: 
                Функция для удаления мероприятия.
            onCloseDetails: 
                Функция для закрытия деталей мероприятия.
            editingEvent: 
                Мероприятие, которое редактируется.
            onUpdateEvent: 
                Функция для обновления мероприятия.
            showTeams: 
                ID мероприятия, для которого отображаются команды.
            onToggleTeams: 
                Функция для переключения отображения команд.
        Рендеринг:
                Отображается список мероприятий.
                Каждое мероприятие отображается в виде блока с названием и датой.
                При клике на мероприятие вызывается функция onViewDetails.
                Кнопки "Редактировать" и "Удалить" отображаются в виде картинок.
                Если selectedEvent существует и его id совпадает с id текущего мероприятия, или если editingEvent существует и его id совпадает с id текущего мероприятия, отображаются детали мероприятия.
                Если editingEvent существует и его id совпадает с id текущего мероприятия, отображается форма редактирования с заголовком "Редактировать" и кнопкой "Закрыть" в виде картинки крестика.
                Если editingEvent не существует, отображаются детали мероприятия с кнопками "Список команд" и "Закрыть" в виде картинки крестика.
                Если showTeams совпадает с id текущего мероприятия, отображается список зарегистрированных команд в виде таблицы с четырьмя столбцами.
                Кнопка "Удалить" в списке команд отображается в виде картинки и удаляет команду при нажатии.            
            
            
            
EventForm.js:
     Состояние:
         name, date, description, teamCount: 
            Значения полей формы.
         useEffect:
            Если event существует, устанавливает значения полей формы.
     Функции:
         handleSubmit: 
            Обрабатывает отправку формы, создает или обновляет мероприятие и вызывает функцию onAddEvent.
    Рендеринг:
            Отображается форма с полями для ввода названия, даты, описания мероприятия и количества команд.
            Кнопка "Сохранить" или "Добавить" для отправки формы.
            Кнопка "Закрыть" для закрытия формы.                
            */